<?php
$a = 5;

$a += 2;  // Tương đương: $a = $a + 2;  ($a bây giờ là 7)
echo "a += 2: " . $a . "<br>";

$a -= 1;  // Tương đương: $a = $a - 1;  ($a bây giờ là 6)
echo "a -= 1: " . $a . "<br>";

$a *= 3;  // Tương đương: $a = $a * 3;  ($a bây giờ là 18)
echo "a *= 3: " . $a . "<br>";

$a /= 2;  // Tương đương: $a = $a / 2;  ($a bây giờ là 9)
echo "a /= 2: " . $a . "<br>";

$a %= 4;  // Tương đương: $a = $a % 4;  ($a bây giờ là 1)
echo "a %= 4: " . $a . "<br>";
?>