<?php
$x = 10;
$y = 3;

$tong = $x + $y;     // Phép cộng: $tong = 13
$hieu = $x - $y;     // Phép trừ: $hieu = 7
$tich = $x * $y;     // Phép nhân: $tich = 30
$thuong = $x / $y;   // Phép chia: $thuong = 3.333...
$du = $x % $y;      // Phép chia lấy dư (Modulo): $du = 1

echo "Tổng: " . $tong . "<br>";
echo "Hiệu: " . $hieu . "<br>";
echo "Tích: " . $tich . "<br>";
echo "Thương: " . $thuong . "<br>";
echo "Số dư: " . $du . "<br>";
?>